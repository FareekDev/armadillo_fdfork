/* macros to enable cleaner "signed" hex in format strings */
#define S_X "%s%#x"
#define S_LX "%s%#lx"

#define S_A(x) ((int)x) < 0 ? "-" : "", ((int)x) < 0 ? -((int)x) : ((int)x)
#define S_LA(x) ((long)x) < 0 ? "-" : "", ((long)x) < 0 ? -((long)x) : ((long)x)

struct ad_operand {
    int type;
    
    /* type == AD_OP_REG */
    struct {
        int rn;
        int sz;
        int zr;
        int sysreg;
        
        const char **rtbl;
    } op_reg;

    /* type == AD_OP_SHIFT */
    struct {
        int type;
        int amt;
    } op_shift;

    /* type == AD_OP_IMM */
    struct {
        int type;
        long bits;
    } op_imm;

    /* type == AD_OP_MEM */
    struct {
        int rn;
        int off;
    } op_mem;
};

struct ad_insn {
    /* string which holds the decoded instr */
    char *decoded;

    int group;
    int instr_id;

    int *fields;
    int num_fields;

    struct ad_operand *operands;
    int num_operands;

    /* code condition */
    int cc;
};

